public class ConsoleColors {
  // Reset
  public static final String RESET = "\033[0m"; // Text Reset

  // Clear Console
  public static final String CLEAR = "\033[H\033[2J";

  // Regular Colors
  public static final String BLACK = "\033[0;30m"; // BLACK
  public static final String RED = "\033[0;31m"; // RED
  public static final String GREEN = "\033[0;32m"; // GREEN
  public static final String YELLOW = "\033[0;33m"; // YELLOW
  public static final String BLUE = "\033[0;34m"; // BLUE
  public static final String PURPLE = "\033[0;35m"; // PURPLE
  public static final String CYAN = "\033[0;36m"; // CYAN
  public static final String WHITE = "\033[0;37m"; // WHITE

  // Bold
  public static final String BBLACK = "\033[1;30m"; // BOLD BLACK
  public static final String BRED = "\033[1;31m"; // BOLD RED
  public static final String BGREEN = "\033[1;32m"; // BOLD GREEN
  public static final String BYELLOW = "\033[1;33m"; // BOLD YELLOW
  public static final String BBLUE = "\033[1;34m"; // BOLD BLUE
  public static final String BPURPLE = "\033[1;35m"; // BOLD PURPLE
  public static final String BCYAN = "\033[1;36m"; // BOLD CYAN
  public static final String BWHITE = "\033[1;37m"; // BOLD WHITE

  // Cards
  public static final String CLUB = "\u2663"; // CLUB
  public static final String DIAMOND = "\u2666"; // DIAMOND
  public static final String SPADE = "\u2660"; // SPADE
  public static final String HEART = "\u2665"; // HEART
}